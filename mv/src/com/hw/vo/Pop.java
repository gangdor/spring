package com.hw.vo;

public class Pop { 
	private String gu;
	private String dong;
	private int total;
	private int one;
	private int two;
	private int three;
	private int four;
	private int five;
	private int six;
	private int sevenover;
	
	public Pop() {
		super();
	}
	
	public Pop(String gu, String dong, int total, int one, int two, int three, int four, int five, int six,
			int sevenover) {
		super();
		this.gu = gu;
		this.dong = dong;
		this.total = total;
		this.one = one;
		this.two = two;
		this.three = three;
		this.four = four;
		this.five = five;
		this.six = six;
		this.sevenover = sevenover;
	}
	public String getGu() {
		return gu;
	}
	public void setGu(String gu) {
		this.gu = gu;
	}
	public String getDong() {
		return dong;
	}
	public void setDong(String dong) {
		this.dong = dong;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public int getOne() {
		return one;
	}
	public void setOne(int one) {
		this.one = one;
	}
	public int getTwo() {
		return two;
	}
	public void setTwo(int two) {
		this.two = two;
	}
	public int getThree() {
		return three;
	}
	public void setThree(int three) {
		this.three = three;
	}
	public int getFour() {
		return four;
	}
	public void setFour(int four) {
		this.four = four;
	}
	public int getFive() {
		return five;
	}
	public void setFive(int five) {
		this.five = five;
	}
	public int getSix() {
		return six;
	}
	public void setSix(int six) {
		this.six = six;
	}
	public int getSevenover() {
		return sevenover;
	}
	public void setSevenover(int sevenover) {
		this.sevenover = sevenover;
	}
	
}






